package j2w.team.modules.screen;

/**
 * @创建人 sky
 * @创建时间 16/2/27
 * @类描述
 */
public class J2WActivityExtra {

    private String key;
    private String value;

    public J2WActivityExtra(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

}
